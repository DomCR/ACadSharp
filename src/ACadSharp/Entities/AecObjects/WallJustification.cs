/// <summary>
/// Defines the justification of a wall.
/// </summary>
public enum WallJustification
{
	/// <summary>
	/// Wall is justified at the left side.
	/// </summary>
	Left = 0,

	/// <summary>
	/// Wall is justified at the center.
	/// </summary>
	Center = 1,

	/// <summary>
	/// Wall is justified at the right side.
	/// </summary>
	Right = 2,

	/// <summary>
	/// Wall is justified at the baseline.
	/// </summary>
	Baseline = 3
}