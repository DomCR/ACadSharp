namespace ACadSharp.Header
{
	public enum XClipFrameType
	{
		/// <summary>
		/// No clipping boundary is displayed or plotted.
		/// </summary>
		None = 0,
		/// <summary>
		/// Clipping boundary is displayed and plotted.
		/// </summary>
		DisplayAndPlot = 1,
		/// <summary>
		/// Clipping boundary is displayed but not plotted.
		/// </summary>
		DisplayNotPlot = 2
	}
}