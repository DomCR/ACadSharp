namespace ACadSharp.Tables
{
	/// <summary>
	/// Represents the fraction format when the linear unit format is set to architectural or fractional.
	/// </summary>
	public enum FractionFormat : short
	{
		/// <summary>
		/// Horizontal stacking.
		/// </summary>
		Horizontal,
		/// <summary>
		/// Diagonal stacking.
		/// </summary>
		Diagonal,
		/// <summary>
		/// Not stacked.
		/// </summary>
		None,
	}
}
